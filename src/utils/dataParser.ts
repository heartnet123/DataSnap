interface SurveyResponse {
  timestamp: string;
  age: string;
  favoriteFruits: string[];
  purchaseLocation: string[];
  selectionCriteria: string[];
  expectedPrice: string;
  eatenWith: string[];
  preferredTime: string[];
  preferredShopLocation: string[];
}

export const parseData = (rawData: string): SurveyResponse[] => {
  const lines = rawData.trim().split("\n");
  // Skip the header row
  const dataRows = lines.slice(1);

  return dataRows
    .map((row) => {
      const columns = row.split("\t");

      // Handle missing data or invalid formats
      if (columns.length < 9) {
        console.warn("Invalid row format:", row);
        return null;
      }

      return {
        timestamp: columns[0],
        age: columns[1],
        favoriteFruits: columns[2].split(", "),
        purchaseLocation: columns[3].split(", "),
        selectionCriteria: columns[4].split(", "),
        expectedPrice: columns[5],
        eatenWith: columns[6].split(", "),
        preferredTime: columns[7].split(", "),
        preferredShopLocation: columns[8].split(", "),
      };
    })
    .filter((item) => item !== null) as SurveyResponse[];
};

export const getAgeCounts = (data: SurveyResponse[]) => {
  const counts: Record<string, number> = {};

  data.forEach((item) => {
    counts[item.age] = (counts[item.age] || 0) + 1;
  });

  return Object.entries(counts).map(([name, value]) => ({ name, value }));
};

export const getFruitCounts = (data: SurveyResponse[]) => {
  const counts: Record<string, number> = {};

  data.forEach((item) => {
    item.favoriteFruits.forEach((fruit) => {
      if (fruit && fruit !== "หนึ่ง" && fruit !== "หำ") {
        // Filter out invalid entries
        counts[fruit] = (counts[fruit] || 0) + 1;
      }
    });
  });

  return Object.entries(counts)
    .map(([name, value]) => ({ name, value }))
    .sort((a, b) => b.value - a.value)
    .slice(0, 10); // Get top 10 fruits
};

